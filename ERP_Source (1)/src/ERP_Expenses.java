    private void btnFilleSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilleSaveActionPerformed
        // TODO add your handling code here:
        try{
            BufferedWriter bfw = new BufferedWriter(new FileWriter("expenses.txt"));
            for(int i = 0 ; i < tableExpenses.getColumnCount() ; i++) {
                bfw.write(tableExpenses.getColumnName(i));
                bfw.write("\t");
            }

            for (int i = 0 ; i < tableExpenses.getRowCount(); i++)
            {
                bfw.newLine();
                for(int j = 0 ; j < tableExpenses.getColumnCount();j++)
                {
                    bfw.write((String)(tableExpenses.getValueAt(i,j)));
                    bfw.write("\t");;
                }
            }
            bfw.close();
            System.out.println("파일 저장 완료");
        }catch(Exception e) {
            System.out.println("FileException : " + e.getMessage());
        }
    }//GEN-LAST:event_btnFilleSaveActionPerformed

