    public void setTableExpensesDB(String strQuery) {
        DefaultTableModel modelAsset = (DefaultTableModel)tableExpenses.getModel();
        modelAsset.setNumRows(0);
        String ExpensesSQL = "select * from expenses";
        try{
            DBM.DB_rs = DBM.DB_stmt.executeQuery(ExpensesSQL);
            while(DBM.DB_rs.next()) {
                    Vector myVC = new Vector();
                    myVC.addElement(DBM.DB_rs.getString("expenses_no"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_code"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_name"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_date"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_cost"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_account"));
                    myVC.addElement(DBM.DB_rs.getString("expenses_remark"));
                    modelAsset.addRow(myVC);
            }
            DBM.DB_rs.close();
        } catch(Exception e) {
            System.out.println("SQLException : " + e.getMessage());
        }
    }
    
