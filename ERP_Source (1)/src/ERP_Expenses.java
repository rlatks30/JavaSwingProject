            setTableExpensesDB(strSQL);
            DBM.dbClose();

            DBM.dbOpen();
            DBM.DB_rs = DBM.DB_stmt.executeQuery("select * from asset");
            int capitalAsset = 0;
            while(DBM.DB_rs.next()){
                capitalAsset = DBM.DB_rs.getInt("capital");
            }
            capitalAsset -= Integer.parseInt(txtExpensesCost.getText());
