    public void getDBData(String strQuery){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setNumRows(0);
        model.setNumRows(31);
        iCntRow = 0;
        try{
            DBM.DB_rs = DBM.DB_stmt.executeQuery(strQuery);
            while(DBM.DB_rs.next()){
                jTable1.setValueAt(DBM.DB_rs.getString("emp_code"), iCntRow, 0);
                jTable1.setValueAt(DBM.DB_rs.getString("salary_date"), iCntRow, 1);
                jTable1.setValueAt(DBM.DB_rs.getString("dept_no"), iCntRow, 2);
                jTable1.setValueAt(DBM.DB_rs.getString("salary_amount"), iCntRow, 3);
                iCntRow++;
            }
            DBM.DB_rs.close();
        }catch (Exception e){
            System.out.println("SQLException: "+ e.getMessage());
        }
    }
    public void calcul(){ //급여 계산해서 pay 리스트에 넣어줌
        try{
            DBM.DB_rs = DBM.DB_stmt.executeQuery("Select * From Salary ");
            while(DBM.DB_rs.next()){
                int salary = 0;
                int ranksal = 0;
                salary = Integer.parseInt(DBM.DB_rs.getString("year"));
                if(DBM.DB_rs.getString("emprank") == "PRESIDENT")
                    ranksal = 10000000;
                if(DBM.DB_rs.getString("emprank") == "MANAGER")
                    ranksal = 5000000;
                if(DBM.DB_rs.getString("emprank") == "ANALYST")
                    ranksal = 4000000;
                if(DBM.DB_rs.getString("emprank") == "SALESMAN")
                    ranksal = 3000000;
                if(DBM.DB_rs.getString("emprank") == "CLERK")
                    ranksal = 2000000;
                pay.add((salary * 1000000) + ranksal);  
            }
            DBM.DB_rs.close();
        }catch (Exception e){
            System.out.println("SQLException: "+ e.getMessage());
        }
    }

